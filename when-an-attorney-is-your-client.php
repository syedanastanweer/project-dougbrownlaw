							<h3 class="title pt-34 pb-20">When an Attorney is Your Client</h3>

							<p class="margin-0">Put yourself back in the leather chair, and imagine for a minute that
								another lawyer has walked into your office and has hired you to represent them.</p>
							<p>What are some things that are going through your mind? Pride, perhaps, that another
								colleague would pick you to litigate their case.</p>
							<p>Maybe anxiety over being able to do a good job for a colleague, or concern that a peer
								might have higher expectations of you than a layperson.</p>
							<p>All of these, and others, are likely in the mind of the attorney you yourself will hire
								to represent you in your own divorce case.</p>
							<h4>What are your expectations, though?</h4>
							<p>In the ABA publication “How to Start and Build a Law Practice,” Jay Foonberg describes
								the sometimes prickly relationship between a new lawyer and the friends/family that are
								typically their first clients.</p>
							<p>As he describes, the friends and family may not see this as an opportunity to help you
								grow your practice (at full price), but, rather, as the potential source of a discount.
							</p>
							<p>Furthermore, the family/friends may consider that they are doing you a favor by hiring
								you, the new inexperienced lawyer in the first place.</p>
							<p>The young lawyer has not achieved the relative stature that an older, more experienced
								lawyer may have, and of course, there is the “familiarity breeds contempt” phenomenon.
							</p>
							<p>These same thoughts can infiltrate the relationship between you and your lawyer.</p>
							<h4>In my practice, I have a fairly de rigeur speech that I give to potential and new
								clients:</h4>
							<p>You are the boss, I am the hired help. You are the pilot, I am the navigator.</p>
							<p>You make the decisions about where to go, and my job is to tell you if you can get there
								from here, what is the safest or quickest route to get there, if there is enough fuel in
								the plane to get there, are there alternate routes you may want to consider.</p>
							<p>Part of that job may also include telling you if you are flying a perfectly good plane
								into the ground.</p>
							<p>Like every professional, we expect a certain amount of professional deference. In other
								words, presumably we know what we are doing and like to be treated that way. Your own
								lawyer is no different.</p>
							<h4>Expectation of the Attorney Client Relationship</h4>
							<p>Just like doctors are the worst patients, attorneys are the worst clients. Why? We tend
								to not follow advice, self-edit what we tell you, and yes, we sometimes want
								special/different/better treatment.</p>
							<p>Your own attorney wants to make a good impression, as you are their professional
								colleague, and they are likely flattered that you hired them.</p>
							<p>People always wonder what others think about them; lawyers are no exception, and the
								lawyer you hired, if they know you, has probably wondered at some time what you think
								about them.</p>
							<h4>Expectations in the fee arrangement:</h4>
							<p>Are you paying more because you are a lawyer, or do you expect a discount because you are
								a fellow lawyer?</p>
							<p>You could pay more if your finances are more complex than most, especially if you are
								self-employed. You could also end up paying more simply because your attorney feels more
								pressure to live up to the expectations of another attorney.</p>
							<p>On the other hand, you may feel entitled to a discount as a professional courtesy, or you
								may believe that own lawyer won’t have to do as much work because you “speak their
								language,” and you know about the discovery process (if you are a litigator). Resolve
								these questions up front.</p>
							<h4>Expectations in communication:</h4>
							<p>Resist the temptation to self-edit based on what you think the situation calls for. As
								lawyers, we are accustomed to analyzing information critically, filtering out what’s
								unnecessary, and presenting only what we deem relevant or advantageous (i.e. “massaging
								the facts”).</p>
							<p>This skill is invaluable in an argument, but is a double-edged sword in real life when
								you’re the client. The attorney-client relationship is built on trust and transparency.
								If you self-edit, your attorney may sense that you’re not being fully forthcoming, which
								can erode trust.</p>
							<p>This can create a barrier to open communication, making it more difficult for your
								attorney to advocate effectively on your behalf. Remember that your attorney is there to
								help you, not to judge you.</p>
							<p>Your attorney needs to know the full truth, even the uncomfortable parts, to prepare and
								to avoid being blindsided.</p>
							<p>Transparency is critical. Trust your attorney to handle the full truth professionally and
								strategically, without judgment.</p>
							<h4>Expectations regarding professional independence:</h4>
							<p>Are you smarter/better than the other attorney? Maybe you are, maybe you aren’t, but your
								attorney will always have something you don’t—objectivity.</p>
							<p>When one lawyer hires another, there is a natural inclination to compare skills,
								knowledge, experience, etc. It’s not uncommon for the lawyer-client to speculate whether
								they are smarter or more capable than the attorney they’ve hired. After all, as a
								lawyer, you’re used to taking control, and it is extremely difficult to let go of
								control when you have so much “skin in the game.</p>
							<p>There may be a tendency to wonder why your own attorney isn’t as upset about “issue X” as
								you are.</p>
							<p>Remember, the objectivity and independence of your attorney are assets, no matter how
								smart you may be, no matter how skilled you are, you will never be able to step outside
								of yourself and fully utilize your analytical skills the same way a different person can
								tap into their own.</p>
							<h4>Final considerations</h4>
							<p>Remember that your attorney, like you, is only a human being.</p>
							<p>They can experience stress and make mistakes, and may have other urgent matters, either
								personal or professional, that happen from time to time.</p>
							<p>Be candid and transparent, even if you are aggravated about something, and relate to them
								as much as possible as the professional they are.</p>


